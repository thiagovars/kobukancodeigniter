							<strong>Postura de guarda (kamae):</strong>
							<p class="tall">Aikido é fundamentalmente equilíbrio. A postura de guarda no Aikido é chamada de 
								kamae. Existem algumas variações de kamae, o kamae básico apresenta a perna 
								esquerda à frente, os pés ficam afastados numa distância de conforto com 90 graus 
								em relação um ao outro, joelhos levemente flexionados, cabeça e coluna devem ser 
