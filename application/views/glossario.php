							<h3 class="push-top">Glossário: <strong>Etiqueta do KOBUKAN DOJO</strong></h3>
						</div>
					</div>

					<div class="row">
						<div class="col-md-12">
							<p class="drop-caps">

								A prática do Aikido tradicional compreende o estudo de seus componentes marcial e filosófico,
								por este motivo, o Aikido é classificado como um "Budo" e não simplesmente como uma arte
								marcial. Para entender a filosofia do Aikido é necessário praticar seus rituais e atentar para seu
								significado. Nós ocidentais com frequência confundimos os gestos de reverência e respeito
								com devoção religiosa. O Aikido não é uma religião, seus rituais demonstram reverência,
								respeito e gratidão ao espaço de treino (Tatâmi), ao altar (Shomen ou Kamidana), à memória
								do fundador (Kaiso), ao representante máximo do Aikido (Doshu), ao instrutor (Sensei), aos
								praticantes (o tangai). Cada dojo é livre para estabelecer seu próprio código de conduta sem,
								no entanto afastar-se da etiqueta tradicional japonesa. Esta flexibilidade é necessária, pois nós
								ocidentais temos costumes muito distintos dos orientais.
							</p>
							<p class="tall">
								Nosso código de conduta compreende:
							<h4 class="shorter">1. Pontualidade:</h4>
							<ul>
								<li>
									Ser pontual é uma questão de respeito, o sensei jamais deve esperar pelos 

									alunos. Quando for inevitável chegar atrasado, o aluno deve postar-se em 

									“Seiza” na lateral do tatâmi, solicitar a atenção do sensei com uma saudação 

									(“O-NEGAI SHIMASU”) e aguardar sua autorização (“DOZO”) para então 

									juntar-se aos demais praticantes.
								</li>
							</ul>

							<h4 class="shorter">2. Cuidados com o dojo:</h4>
							<ul>
								<li>A entrada e saída do tatâmi devem ser precedidas de uma reverência.</li>
								<li>
									Manter o dojo limpo evitando jogar papeis no chão e deixando garrafas de 
									água espalhadas pelo ambiente. Os alunos devem também zelar pela limpeza 
									nos banheiros e vestiários.
								</li>
							</ul>

								<h4 class="shorter">3. Cuidados com a aparência e higiene:</h4>
							<ul>
								<li>O aluno deve manter seu dogi limpo, as unhas sempre curtas e tratar de odores.</li>
								<li>Durante os ritos de abertura e encerramento e sempre que possível deve-se arrumar o dogi.</li>
								<li>
									As mulheres devem usar um top ou camiseta por baixo do dogi de preferência 
									na cor branca. Os homens também podem se utilizar de uma camiseta por 
									baixo do dogi.
								</li>
								<li>
									O Hakama é obrigatório para faixa preta e opcional para mulheres a partir do 
									4o kyu (faixa roxa).
								</li>
								<li>Nunca andar pelas dependências do dojo sem camisa, camiseta ou regata.</li>
							</ul>

								<h4 class="shorter">4. Rito de abertura:</h4>
							<ul>
								
								<li>
									Todos em seiza, os alunos ordenados em ordem crescente de graduação e 
									tempo de treino da direita para a esquerda (em relação ao Kamidana) e de
									frente para o sensei fazem uma breve meditação.
								</li>
								<li>
									Passado um tempo, o instrutor determina o final da meditação dizendo 
									“MOKUSO YAME” ou simplesmente “YAME”. Seguem-se uma sequência de
									reverências (REI).
								</li>
								<li>
									Os dois próximos cumprimentos são opcionais, pois envolvem crenças 
									religiosas. Algumas pessoas podem se sentir desconfortáveis com esse tipo
									de reverência, por isso não é um procedimento obrigatório, assim cada um 
									pode reverenciar aquele em que crê. Em alguns dojos essas reverências são 
									precedidas por uma oração xintoísta.
								</li>

								<li>
									<ul>
										<strong>NÃO OBRIGATÓRIO:</strong>

										<li>
											<ul>
												<li>
													Todos fazem duas reverencias (Reverência ao altar, ao fundador do 
													Aikido Ô-Sensei Morihei Ueshiba e aos seus sucessores). Todos os 
													participantes batem palmas duas vezes.
												</li>
												<li>
													<strong>OPCIONAL:</strong> O sensei pode optar por realizar uma reza xintoísta e ao 
													final todos os participantes batem palmas duas vezes.
												</li>
												<li>
													Realiza-se uma ultima reverencia ao altar (Kamidana).
												</li>
											</ul>
										</li>
									</ul>
								</li>
								<li>
									Por fim todos praticantes fazem uma reverencia ao sensei e dizem “O-NEGAI 
									SHIMASU” (Por Favor, ou, em tradução literal “Entrego minha vida em suas mãos”).
								</li>
							</ul>

								<h4 class="shorter">5. Durante o treino:</h4>
							<ul>
								<li>Deve-se prestar atenção às explanações do sensei e em caso de dúvida 
								levantar a mão e aguardar. Se houver duvidas durante o treino ir até o sensei, 
								nunca chamá-lo.</li>

								<li>Ao ser chamado pelo sensei fazer uma reverência e dizer “O-NEGAI SHIMASU”.</li>

								<li>Ao iniciar uma demonstração ou treino realizar uma reverência e dizer “O-NEGAI SHIMASU”.</li>

								<li>Ao final de uma demonstração ou treino agradecer com uma reverência e dizer </li>

								<li>Manter uma atitude positiva de cuidado com a segurança do parceiro de treino </li>

								<li>Nunca corrigir um colega mais graduado.</li>

								<li>Nunca atravessar na frente de alguém, sempre dar a volta por trás.</li>

								<li>Se necessitar sair do tatâmi solicitar permissão ao sensei. O mesmo deve ser </li>

								<li>Não cruzar os braços (para o japonês esta é uma atitude de desafio).</li>

							</ul>
								<h4 class="shorter">6. Rito de encerramento:</h4>
							<ul>
								<li>Semelhante ao rito de abertura, podendo haver uma meditação mais demorada.</li>

								<li>
									Ao final as reverências são feitas para o sensei e para os alunos mais
									graduados até que os menos graduados se reverenciam entre si, a cada 
									reverência diz-se “DOMO ARIGATOO GOZAIMASHITA” (Muito obrigado, agradecido).
								</li>

								<li>Em seguida um reverência feita para o sensei por todos os alunos dizendo “SAYONARA SENSEI” (Adeus ou Até logo).</li>

								<li>Por fim todos se cumprimentam (ao estilo ocidental) com um aperto de mãos e / ou um abraço.</li>
							</ul>

								<h4 class="shorter">7. Exames de kyu (mudança de faixa):</h4>
							<ul>
								<li>
									O sensei (exclusivamente) determinará quando o aluno estará apto a fazer o exame de faixa, com base na evolução e no tempo de treino do aluno.
								</li>
								<li>
									Será indicado ao postulante um Uke, preferencialmente no seu mesmo nível.
								</li>
								<li>
									O postulante deve adquirir o vocabulário básico usado no Aikido, como números, nomenclatura dos katas dentre outros.
								</li>
								<li>
									Durante o exame, o postulante permanecerá em silêncio e realizará os movimentos solicitados pelo sensei.
								</li>
								<li>
									Além da execução dos katas serão avaliadas a postura e a conduta do postulante como apresentação, equilíbrio, forma de deslocar-se e o
									cumprimento da etiqueta (saudações).
								</li>
							</ul>

							<p class="tall">Da mesma forma que o aluno escolhe seu sensei, o sensei é livre para escolher seus alunos.</p>

							<p class="tall">O Aikido é uma arte marcial não competitiva que visa a harmonia, o praticante deve se 

								abster da violência e da soberba. A prática desta etiqueta visa preservar o espírito do Aikido 

								tradicional e favorecer o fortalecimento dos vínculos de amizade e respeito no grupo.</p>

