							<h3><strong>Sobre:</strong> Aikido</h3>
							<p class="tall">
								Caminho marcial japonês criado pelo grão-mestre Morihei Ueshiba (1883/1969).
								Caracteriza-se por ser um Budô, ou seja, uma arte marcial dotada de filosofia 
								verdadeira e caminho espiritual: o Aikido não possui competições pois o objetivo não é 
								lutar contra o parceiro mas permitir o desenvolvimento espiritual por meio do exercício 
								das técnicas. 
							</p>
							<p class="tall">
								Essa Arte marcial é baseada em esquivas, arremessos, torções nas articulações e 
								imobilizações dos agressores. É derivado do Daitoryu Aikijiujitsu, arte marcial samurai 
								praticada pelo clã Aizu. Durante séculos ficou restrita a apenas uma família (Takeda) 
								e apenas em 1900 começou a ser ensinada aos militares, a nobreza ou graduados de 
								outros budôs.
							</p>
							<p class="tall">
								É conhecido como a “arte marcial da paz” pois não possui técnicas de ataque, mas 
								apenas de defesa pessoal. Utiliza a espada e o bastão, armas tradicionais dos 
								samurais, como um complemento e aperfeiçoamento das demais técnicas. Pode ser 
								praticado por ambos os sexos, dos 6 aos 80 anos, pois utiliza a força do adversário em 
								detrimento da força bruta.</p>
